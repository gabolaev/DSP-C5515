Int16 set_letter_sumbol(Uint16 letter)
{
    switch(letter)
    {
        case ' ': {
            OSD9616_send(0x40,0x00);
            OSD9616_send(0x40,0x00);
            OSD9616_send(0x40,0x00);
            OSD9616_send(0x40,0x00);
            break;
        }
        case '!': return printLetter_col5(0x00, 0x00, 0x5f, 0x00, 0x00); // !
        case '.': return printLetter_col5(0x00, 0x60, 0x60, 0x00, 0x00); // .
        case ',': return printLetter_col5(0x00, 0x50, 0x30, 0x00, 0x00); // ,
        case '#': return printLetter_col5(0x14, 0x7f, 0x14, 0x7f, 0x14); // #
        case '*': return printLetter_col5(0x14, 0x08, 0x3e, 0x08, 0x14); // *
        case '+': return printLetter_col5(0x08, 0x08, 0x3e, 0x08, 0x08); // +
        case '-': return printLetter_col5(0x08, 0x08, 0x08, 0x08, 0x08); // -
        case '=': return printLetter_col5(0x14, 0x14, 0x14, 0x14, 0x14); // =
        case '?': return printLetter(0x46,0x29,0x19,0x7F);
        case '(': return printLetter_col5(0x1C, 0x36, 0x41, 0x00, 0x00); // (
        case ')': return printLetter_col5(0x00, 0x00, 0x41, 0x36, 0x1C); // )
        case '$': return printLetter_col5(0x24, 0x2a, 0x7f, 0x2a, 0x12); // $
    }
    return 0;
}

Int16 printWord(char *word)
{
    Uint16 len,i;
    len = strlen(word);
    for (i=0;i<len;i++){
        set_letter_en(word[len-1-i]);
        set_letter_rus(word[len-1-i]);
        set_letter_num(word[len-1-i]);
        set_letter_sumbol(word[len-1-i]);
    }


    return 0;
}

void clear_row(void){
    Uint16 i;
    for(i=0;i<127;i++)
        OSD9616_send(0x40,0x00);
    OSD9616_send(0x00,0x00);   // Set low column address
    OSD9616_send(0x00,0x10);   // Set high column address
}

void clear_disp(void) //clean LCD display
{
    switchToUpperRow();
        clear_row();
    switchToLowerRow();
        clear_row();
}
